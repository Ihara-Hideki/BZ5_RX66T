#include  "custom.h"

void ini_TIOP5_member(void)
{
     TOPEEN     = 0x0000;              //外部イネーブルの禁止

     FFS1       = (FFS1 & 0xfc);
     FFS1       = (FFS1 | 0x00);

     TIOPRO     = (TIOPRO & 0xffdf);   //プロテクトを解除する
     TIOCEN     = (TIOCEN & 0xffdf);   //カウントを停止する

     TIO5CR     = 0xC2;
     TIO5CT     = 312;
     TIO5RL0    = 312;
     TIO5RL1    = 312;
     return;
}
