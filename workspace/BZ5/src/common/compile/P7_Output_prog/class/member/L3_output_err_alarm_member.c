#include  "custom.h"

void output_err_alarm_member(void)
{
     //エラー出力
     if (gub_Err == ON)
     {
//2008.03.27　リレー対策         ERR_OUT = ON;
         ERR_OUT = ON;  //2008.10.30復活
     } else {
//2008.03.27　リレー対策         ERR_OUT = OFF;
         ERR_OUT = OFF; //2008.10.30復活
     }

     //アラーム出力
     if (gub_Alarm == ON)
     {
//2008.03.27　リレー対策         ALRM_OUT = ON;
         ALRM_OUT = ON;   //2008.10.30復活
     } else {
//2008.03.27　リレー対策         ALRM_OUT = OFF;
         ALRM_OUT = OFF;  //2008.10.30復活
     }

     return;
}
